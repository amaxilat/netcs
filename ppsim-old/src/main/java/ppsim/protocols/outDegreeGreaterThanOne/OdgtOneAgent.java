package ppsim.protocols.outDegreeGreaterThanOne;

import ppsim.model.AbstractAgent;
import ppsim.model.Population;

/**
 * Implements the agent of the Out-Degree-Greater-Than-1 protocol.
 */
public class OdgtOneAgent extends AbstractAgent<OdgtOneState> {

    /**
     * Default constructor.
     *
     * @param pop the Population holder.
     */
    public OdgtOneAgent(final Population<OdgtOneState> pop) {
        super(pop);
    }

    /**
     * Initializes the state of the agent.
     */
    public void initAgent() {
        setState(OdgtOneState.STATE_INIT);
    }
}
