package ppsim.protocols.twoCycle;

import ppsim.model.AbstractAgent;
import ppsim.model.Population;

/**
 * Implements the agent of the TwoCycle protocol.
 */
public class TwoCycleAgent extends AbstractAgent<TwoCycleState>  {

    /**
     * Default constructor.
     *
     * @param pop the Population holder.
     */
     public TwoCycleAgent(final Population<TwoCycleState> pop) {
        super(pop);
     }

    /**
     * Initializes the state of the agent.
     */
    public void initAgent() {
        setState(TwoCycleState.STATE_LS);
    }
}
