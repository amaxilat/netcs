package ppsim.protocols.le;

import ppsim.model.AbstractAgent;
import ppsim.model.Population;

/**
 * Implements the agent of the Leader Election protocol.
 */
public class LEAgent extends AbstractAgent<Boolean> {

    /**
     * Default constructor.
     *
     * @param pop the Population holder.
     */
    public LEAgent(final Population<Boolean> pop) {
        super(pop);
    }

    /**
     * Initializes the state of the agent.
     */
    public void initAgent() {
        setState(true);
    }

}
